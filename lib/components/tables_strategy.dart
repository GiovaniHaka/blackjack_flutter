class Cedule extends StatelessWidget {
  ///Only Strings "H", "S", "D", "P"
  final String value;

  Cedule({@required this.value});

  @override
  Widget build(BuildContext context) {
    Color _firsColor;
    Color _secondColor;

    if (value == "D") {
      _firsColor = Colors.yellow;
      _secondColor = Colors.yellow[600];
    } else if (value == "S") {
      _firsColor = Colors.red;
      _secondColor = Colors.red[600];
    } else if (value == "H") {
      _firsColor = Colors.green;
      _secondColor = Colors.green[600];
    } else if (value == "P") {
      _firsColor = Colors.blue;
      _secondColor = Colors.blue[600];
    }

    return Container(
        decoration: BoxDecoration(
          border: Border.all(
            color: _secondColor,
            width: 1,
          ),
          color: _firsColor,
        ),
        child: Center(child: Text(value)));
  }
}
